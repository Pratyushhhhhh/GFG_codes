class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        // Code Here
        int max=arr[0],smax=0;
        for (auto it:arr)
        {
            if(max<it)
            {
                smax=max;
                max=it;
            }
            else if(max!=it&&smax<it)
            smax=it;
        }
        if(smax!=max&&smax>0)
        return smax;
        else
        return -1;
    }
};

